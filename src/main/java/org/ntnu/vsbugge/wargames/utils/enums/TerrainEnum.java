package org.ntnu.vsbugge.wargames.utils.enums;

/**
 * An enum used to represent the current terrain.
 *
 * The enum constant DEFAULT indicates that there should be no terrain considerations.
 *
 * @author vsbugge
 */
public enum TerrainEnum {
    DEFAULT, // Default indicates no terrain considerations
    HILL, PLAINS, FOREST
}
